const APIConstantsService = {

    getHeroInfo(id){
        for(const hero of heros){
            if(id === hero['heroId']){
                return hero.info;
            }
        }
        return new Error("Failed to find hero by ID, check if heros constants array is up to date in APIConstantsService")
    },

    getHeroName(id){
        for(const hero of heros){
            if(id === hero['heroId']){
                return hero.info.name;
            }
        }
        return new Error("Failed to find hero by ID, check if heros constants array is up to date in APIConstantsService")

    }, 

    // player slots... 0-127 are Radiant, 128-255 are Dire
    isRadiaint(match){
        return match.player_slot <= 127
    },

    // returns bool to tell if the player won the match. 
    didWinMatch(match){
        if (this.isRadiaint(match) && match.radiant_win){
        return true 
        } else if (!this.isRadiaint(match) && !match.radiant_win){
        return true 
        }
        return false 
    }


}

